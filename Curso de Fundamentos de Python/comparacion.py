

# > (Mayor que)
print(7 > 3)
print(3 > 7)

# > (Menor que)
print(5 < 6)
print(6 < 5)

# >= (Mayor o igual que)
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

# <= (Menor o igual que)
print(1 <= 2)
print(2 <= 1)
print(2 <= 2)

# == (Igual que)
print(6 == 6)
print(5 == 2)

#- != (Diferente que)
print(6 != 10)
print(6 != 6)

# Diferente entre str
print('Apple' == "Apple")
print('Apple' == "apple")

# Diferente entre str y int
print('1' == 1)

# Acceso Edad
age= 18
print(age >= 18)
age= 15
print(age >= 18)